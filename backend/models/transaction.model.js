import mongoose from "mongoose";

const transactionSchema = new mongoose.Schema({
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  paymentType: {
    type: String,
    required: true,
    enum: ["cash", "credit", "debit", "other"],
  },
  location: {
    type: String,
    default: "UNKNOWN",
  },

  amount: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

export default Transaction = mongoose.model("Transaction", transactionSchema);
